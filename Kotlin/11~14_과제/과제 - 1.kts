fun plus(num1 : Int, num2 : Int) : Int {
    return num1 + num2
}
fun minus(num1 : Int, num2 : Int) : Int {
    return num1 - num2
}
fun multefe(num1 : Int, num2 : Int) : Int {
    return num1 * num2
}
fun divide(num1 : Int, num2 : Int) : Int {
    return num1 / num2
}

println(plus(5, 2))
println(minus(5, 2))
println(multefe(5, 2))
println(divide(5, 2))